DATA SEGMENT
	STR DB "PLEASE INPUT A STRING:$" 
	BUFFER DB 100 DUP(?)
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:
	MOV AX, DATA
	MOV DS, AX
	LEA DX, STR		;输出提示字符串
	MOV AH, 9
	INT 21H
	MOV BX, OFFSET BUFFER		;开始输出字符
	MOV AH, 1
	INT 21H
	CMP AL, ' '		;如果第一个字符是空格则直接退出	
	JZ EXIT
	MOV [BX], AL
	INC BX
NEXT:
	MOV AH, 1
	INT 21H
	CMP AL, ' '		;输入的字符若是空格则结束输入
	JZ OUT_ALL
	MOV [BX], AL
	INC BX
	JMP NEXT
OUT_ALL:
	MOV DL, 13		;输出回车
	MOV AH, 2
	INT 21H
	MOV DL, 10		;输出换行
	MOV AH, 2
	INT 21H
	MOV DL, '$'		;输出字符串
	MOV [BX], DL
	LEA DX, BUFFER
	MOV AH, 9
	INT 21H
EXIT:
	MOV AX, 4C00H
	INT 21H
CODE ENDS
END START
	